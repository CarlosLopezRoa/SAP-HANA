CREATE PROCEDURE "RFC_USER"."PR_REFRESH_DASHBOARD_D" (IN DATE_FROM DATE, IN DATE_TO	DATE)
AS BEGIN

DELETE FROM "DASHBOARD_MV_D"
WHERE FECHA BETWEEN :DATE_FROM AND DATE_TO;

INSERT INTO "DASHBOARD_MV_D"
SELECT *  
FROM "_SYS_BIC"."T.Dashboard/CA_MIS"
	(PLACEHOLDER."$$IN_FROM$$" => :DATE_FROM,
	 PLACEHOLDER."$$IN_TO$$" => :DATE_TO );
END;